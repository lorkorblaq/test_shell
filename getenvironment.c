#include "shell.h"

/**
 * getenv() this gets the string array which the environments abide
 */
char **getEnviron(data_p *info)
{
	if (!info->environ || info->env_changed)
	{
		info->environ = list_to_strings(info->env);
		info->env_changed = 0;
	}
	return (info->environ);
}

/**
 * environUnSet - Remove an environment variable
 * @info is a structure of containing arguments
 * Return, 1 on delete, 0 if failed otherwise
 * @var: the string env var property
 */
int environUnSet(data_p *info, char *var)
{
	list_t *node = info->env;
	size_t i = 0;
	char *p;

	if (!node || !var)
		return (0);

	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			info->env_changed = delete_indexNode(&(info->env), i);
			i = 0;
			node = info->env;
			continue;
		}
		node = node->next;
		i++;
	}
	return (info->env_changed);
}
/**
 * environSet - Initialize a new environment variable, or modify an existing one
 *  Return: Always 0
 */
int environSet(data_p *info, char *var, char *value)
{
	char *buf = NULL;
	list_t *node;
	char *p;

	if (!var || !value)
		return (0);

	buf = malloc(_strlen(var) + _strlen(value) + 2);
	if (!buf)
		return (1);
	_strcpy(buf, var);
	_strcat(buf, "=");
	_strcat(buf, value);
	node = info->env;
	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			free(node->str);
			node->str = buf;
			info->env_changed = 1;
			return (0);
		}
		node = node->next;
	}
	NodeEndAdded(&(info->env), buf, 0);
	free(buf);
	info->env_changed = 1;
	return (0);
}
